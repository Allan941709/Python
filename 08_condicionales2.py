#condicionales con elif
# elif es una abreviacion de else if
# elif se utiliza para evaluar multiples condiciones
# elif se puede utilizar multiples veces
ocupacion = "nada"
if ocupacion == "estudiante":
    print("tienes 50% de descuento")
elif ocupacion == "jubilado":
    print("tienes 75% de descuento")
elif ocupacion == "desempleado":
    print("tienes 25% de descuento")
else:
    print("no tienes descuento")


 
    